export function matchesPattern(code: string, pattern: string): boolean {
  const codeUpper = code.toUpperCase().trim();
  const patternUpper = pattern.toUpperCase().trim();

  if (patternUpper === codeUpper) {
    return true;
  }

  if (patternUpper.includes('-')) {
    const [start, end] = patternUpper.split('-').map(p => p.trim());
    const prefix = start.match(/^[A-Z]+/)?.[0] || '';
    const startNum = parseFloat(start.replace(prefix, ''));
    const endNum = parseFloat(end.replace(prefix, ''));
    const codePrefix = codeUpper.match(/^[A-Z]+/)?.[0] || '';
    const codeNum = parseFloat(codeUpper.replace(codePrefix, ''));

    if (prefix === codePrefix && !isNaN(startNum) && !isNaN(endNum) && !isNaN(codeNum)) {
      return codeNum >= startNum && codeNum <= endNum;
    }
  }

  const patternRegex = patternUpper
    .replace(/\./g, '\\.')
    .replace(/X/g, '.')
    .replace(/\*/g, '.*');
  
  const regex = new RegExp(`^${patternRegex}`);
  return regex.test(codeUpper);
}

export function findElixhauserCategory(icd10Code: string): string | null {
  for (const category of elixhauserCategories) {
    for (const pattern of category.patterns) {
      if (matchesPattern(icd10Code, pattern)) {
        return category.category;
      }
    }
  }
  return null;
}
